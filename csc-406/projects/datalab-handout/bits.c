/* 
 * setFirst - returns value with n upper bits set to 1 
 *            and 32-n lower bits set to 0
 *  You may assume 0 <= n <= 32
 *  Example: setFirst(4) = 0xF0000000
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 10
 *  Rating: 2
 */
int setFirst(int n) {
   /*
   Code Breakdown
   --------------
   (~1 + 1)                          -> equates to -1
   n + (~1 + 1)                      ->
   !!n                               -> Double logical not: 
   (!!n & 1) << 31                   ->
   (!!n & 1) << 31) >> (n + (~1 + 1) ->
  */
  return ((!!n & 1) << 31) >> (n + (~1 + 1));
}
/* 
 * rotate4 - Rotate x to the left by 4
 *   Examples: rotate4(0x87654321) = 0x76543218
 *   Legal ops: ~ & ^ | + << >> !
 *   Max ops: 10
 *   Rating: 2 
 */
int rotate4(int x) {
  /*
  Code Breakdown
  --------------

  */
  int rotate = 4;
  return ((x << rotate) | ((x >> (33 + ~rotate)) & ~((~1 + 1) << rotate)));
}
/* 
 * allEvenBits - return 1 if all even-numbered bits in word set to 1
 *   Examples allEvenBits(0xFFFFFFFE) = 0, allEvenBits(0x55555555) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 2
 */
int allEvenBits(int x) {
  /*
  Code Breakdown
  --------------

  */  
  x &= x >> 16;
  x &= x >> 8;
  x &= x >> 4;
  x &= x >> 2;
  return x & 1;
}
/* 
 * rotateLeft - Rotate x to the left by n
 *   Can assume that 0 <= n <= 31
 *   Examples: rotateLeft(0x87654321,4) = 0x76543218
 *   Legal ops: ~ & ^ | + << >> !
 *   Max ops: 25
 *   Rating: 3 
 */
int rotateLeft(int x, int n) { 
  /*
  Code Breakdown
  --------------

  */ 
  return ((x << n) | ((x >> (32 + ~n + 1)) & ~((~1 + 1) << n)));
}
/* 
 * replaceByte(x,n,c) - Replace byte n in x with c
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: replaceByte(0x12345678,1,0xab) = 0x1234ab78
 *   You can assume 0 <= n <= 3 and 0 <= c <= 255
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 3
 */
int replaceByte(int x, int n, int c) {
  /*
  Code Breakdown
  --------------

  */ 
  int mask = 0xff << (n << 3);
  int shift = (c << (n << 3));
  return (~mask & x) | shift;

}
